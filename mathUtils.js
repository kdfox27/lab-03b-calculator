export function add(firstNumber, secondNumber) {
    // firstNumber is a variable that only exists in the function
    // Only in SCOPE of the function.
    return firstNumber + secondNumber;
}

export function sub(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
}

export function mult(firstNumber, secondNumber) {
    return firstNumber * secondNumber;
}

export function div(firstNumber, secondNumber) {
    return firstNumber / secondNumber;
}

